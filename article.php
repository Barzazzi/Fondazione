            <h2 class="blog-post-title">Call for IUPAC Postgraduate Summer School on Green Chemistry</h2>
            <p class="blog-post-meta">Posted on 7-14 July 2018 by <a href="#" class="link">Tundo</a>, Venice Italy</p>
            <img src="articolo1.png" class="img-fluid img-thumbnail" alt="Responsive image">

           <p>
            This Summer School will ideally follow the 10 previous editions held in Venice from 1998 to 2008, but this will be the first one under the <a href="https://iupac.org/" class="link">International Union of Pure and Applied Chemistry IUPAC</a> sponsorship.
           </p>
           <hr>
           <p>
            Green Chemistry is one of the key sciences of a sustainable development on earth and required for reaching the <a href="http://www.un.org/sustainabledevelopment/sustainable-development-goals/" class="link">17 UN-Goals</a>: IUPAC and ICGCSD are fully aware of their responsibility in working in the direction of Sustainable Development through Green Chemistry.
           </p>
           <p>
           The School will be partly hosted in the prestigious <strong>Palazzo Ducale</strong>, the Doge’s Palace, in St. Mark’s Square thanks to the collaboration with the the Superintendence of <a href="http://www.soprintendenza.venezia.beniculturali.it/" class="link">Archeology, Fine Arts and Landscape for the City of Venice and Lagoon</a>.
           </p>
           <p>
           About 80 postgraduate students and 20 teachers coming from all over the world are expected to attend. Scholarships will be offered to young talented chemists coming from developing countries.           
           </p>
           <p>
           The choice of Venice, this extraordinary but delicate architectural masterpiece between land and sea, as Host city for the Summer School has offered the opportunity to highlight the emerging connection of Green Chemistry with cultural heritage restoration and conservation field.           
           </p>
           <h3>Main topics</h3>
            <ul>
                <li>Chemistry beyond chlorine</li>
                <li>New reaction pathways</li>
                <li>Exploitation of renewable resources</li>
                <li>Green Chemistry for cultural heritage restoration</li>
            </ul>
